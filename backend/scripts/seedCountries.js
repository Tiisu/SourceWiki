async function seedCountries() {
  try {
    // Connect to MongoDB using the same config as the server
    const mongoUri = process.env.MONGODB_URI || config.mongodbUri || 'mongodb://localhost:27017/wikisourceverifier';
    
    if (!mongoUri) {
      console.error('‚ùå Error: MONGODB_URI environment variable is not set');
      console.error('üí° Please set MONGODB_URI in your .env file or as an environment variable');
      process.exit(1);
    }
    
    console.log('üîå Connecting to MongoDB...');
    await mongoose.connect(mongoUri);
    console.log('‚úÖ Connected to MongoDB');

    let created = 0;
    let skipped = 0;

    // Seed countries
    for (const country of ALL_COUNTRIES) {
      try {
        const existing = await CountryStats.findOne({ countryCode: country.code.toUpperCase() });
        if (existing) {
          // Update name if it changed
          if (existing.countryName !== country.name) {
            existing.countryName = country.name;
            await existing.save();
            console.log(`üìù Updated: ${country.name} (${country.code})`);
          } else {
            skipped++;
          }
        } else {
          await CountryStats.create({
            countryCode: country.code.toUpperCase(),
            countryName: country.name
          });
          created++;
          console.log(`‚úÖ Created: ${country.name} (${country.code})`);
        }
      } catch (error) {
        console.error(`‚ùå Error processing ${country.name} (${country.code}):`, error.message);
      }
    }

    console.log('\nüìä Seeding Summary:');
    console.log(`   Created: ${created}`);
    console.log(`   Skipped (already exist): ${skipped}`);
    console.log(`   Total: ${ALL_COUNTRIES.length}`);

    await mongoose.disconnect();
    console.log('\n‚úÖ Disconnected from MongoDB');
    process.exit(0);
  } catch (error) {
    console.error('\n‚ùå Seeding error:', error.message);
    
    if (error.name === 'MongooseServerSelectionError' || error.message.includes('ECONNREFUSED')) {
      console.error('\nüí° MongoDB connection failed. Please ensure:');
      console.error('   1. MongoDB is running on your system');
      console.error('   2. MONGODB_URI in .env file is correct');
      console.error('   3. MongoDB is accessible at the specified host and port');
      console.error('\n   Example MONGODB_URI: mongodb://localhost:27017/wikisourceverifier');
      console.error('   Or for MongoDB Atlas: mongodb+srv://username:password@cluster.mongodb.net/dbname');
    }
    
    process.exit(1);
  }
}

// Run the seeder
seedCountries();

